class METARIG_OT_add_rig(bpy.types.Operator):
    bl_description = "Create a metarig object with better defaults"
    bl_idname = 'zpy.add_metarig'
    bl_label = "Human (Template) "
    bl_options = {'UNDO'}

    @classmethod
    def description(cls, context, properties):
        return cls.bl_rna.description

    @classmethod
    def poll(cls, context):
        return True

    def execute(self, context):
        active_mode = context.mode

        # Add armature object
        obj = New.armature(context, "metarig")
        obj.data.name = "metarig"
        obj.location = Get.cursor(context).location

        # Create metarig
        Set.active_select(context, obj, True)
        Set.mode(context, obj, 'EDIT')
        create(obj)

        bpy.ops.object.mode_set(mode=active_mode)

        return {'FINISHED'}


def add_menu(self, context):
    layout = self.layout
    layout.operator('zpy.add_metarig', icon='OUTLINER_OB_ARMATURE')


